const API_BASE = 'http://localhost:5678/auth';

export async function signup(data) {
    const response = await fetch(`${API_BASE}/signup`, {
        method: 'POST',
        headers: {
            'Content-Type': 'application/json'
        },
        body: JSON.stringify(data)
    });
    return response.json();
}

